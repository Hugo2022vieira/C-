using System;

public class LerNumero
{
    public static void Main(string[] args)
    {
        // Declara uma variável para armazenar o número lido
        int numero;

        // Lê o número do teclado
        Console.WriteLine("Digite um número: ");
        numero = int.Parse(Console.ReadLine());

        // Imprime o número na tela
        Console.WriteLine("O número lido é: {0}", numero);
    }
}


/* 
Exercício 1: Ler e imprimir um número

Objetivo: O objetivo deste exercício é aprender a ler um número do teclado e imprimi-lo na tela.

*/